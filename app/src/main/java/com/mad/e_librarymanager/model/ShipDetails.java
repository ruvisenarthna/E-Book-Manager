package com.mad.e_librarymanager.model;

public class ShipDetails {
    private String name;
    private String address;
    private String no;
    private String exp_del;
    private String exp_arr;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getExp_del() {
        return exp_del;
    }

    public void setExp_del(String exp_del) {
        this.exp_del = exp_del;
    }

    public String getExp_arr() {
        return exp_arr;
    }

    public void setExp_arr(String exp_arr) {
        this.exp_arr = exp_arr;
    }
}
